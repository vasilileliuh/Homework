public class BackslashEscape{

     public static void main(String... args){

         System.out.print("    \\/ \n");
         System.out.print("  \\ \\/ / \n");
         System.out.print("\\ \\ \\/ / / \n");
         System.out.print("/ / /\\ \\ \\ \n");
         System.out.print("  / /\\ \\ \n");
         System.out.print("    /\\ \n");
         System.out.print(" \n");

     }
}